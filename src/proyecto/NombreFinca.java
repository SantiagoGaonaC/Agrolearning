    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PantallaDeCargaGrande f = new PantallaDeCargaGrande();
        f.setVisible(true);
        this.setVisible(false);
        String nf = this.txtNombres.getText();
        if (sqlmod.registrarPartida(usur, nf)) {
            sqlmod.BuscarPartida(usur);
            sqlmod.RegistrarPrecios(usur, 0, 0, 0);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error en la base de datos ");
        }
        VentanaCliente1 vc;
        try {
            vc = new VentanaCliente1(usur, sqlmod.CargarDatosInicioCultivos(usur), sqlmod,
                    sqlmod.NumeroPartida(usur), sqlmod.CultivosCOmpa(),
                     sqlmod.Estados(),sqlmod.IDNumeroOfertaDeManda(usur));
            f.dispose();
            vc.setVisible(true);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error en la conexi√≥n");
            this.setVisible(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed
