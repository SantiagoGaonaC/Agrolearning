    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int index = ComBoxSql2.getSelectedIndex();
        switch (index) {
            case 0:
                String[] columnNames = {"Nombre del cultivo", "Semana", "Altura cultivo", "Agua gastada", "Estado"};
                if (this.TxtVatriable.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Ingrese un usuario a buscar");
                } else {
                    if (modsql.VerificarNombre(this.TxtVatriable.getText())) {
                        Object[][] datosv = modsql.CargarUsuariosCultivos(this.TxtVatriable.getText());
                        DefaultTableModel dtm = new DefaultTableModel(datosv, columnNames);
                        this.TablaSql.setModel(dtm);
                        this.TablaSql.setBackground(new Color(184, 242, 190));
                    } else {
                        JOptionPane.showMessageDialog(null, "Usuario no encontrado");
                    }

                }
                break;
            case 1:
                String[] columnNames1 = {"Cantidad de ventas", "Capital total", "cantidad de prouctos acomulados"};
                if (this.TxtVatriable.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Ingrese un usuario a buscar");
                } else {
                    if (modsql.VerificarNombre(this.TxtVatriable.getText())) {
                        Object[][] datosv = modsql.CargarUsuariosProceso(this.TxtVatriable.getText());
                        DefaultTableModel dtm = new DefaultTableModel(datosv, columnNames1);
                        this.TablaSql.setModel(dtm);
                        this.TablaSql.setBackground(new Color(184, 242, 190));
                    } else {
                        JOptionPane.showMessageDialog(null, "Usuario no encontrado");
                    }

                }
                break;
            case 2:
                String[] columnNames2 = {"Entradas", "Salidas"};
                if (this.TxtVatriable.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Ingrese un usuario a buscar");
                } else {
                    if (modsql.VerificarNombre(this.TxtVatriable.getText())) {
                        Object[][] datosv = modsql.CargarUsuariosRegsitros(this.TxtVatriable.getText());
                        DefaultTableModel dtm = new DefaultTableModel(datosv, columnNames2);
                        this.TablaSql.setModel(dtm);
                        this.TablaSql.setBackground(new Color(184, 242, 190));
                    } else {
                        JOptionPane.showMessageDialog(null, "Usuario no encontrado");
                    }

                }

                break;
        }


    }//GEN-LAST:event_jButton1ActionPerformed

