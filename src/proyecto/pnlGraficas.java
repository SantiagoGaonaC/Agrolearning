    public void CargarLaVaina() {
        SQLUsuarios modSQL = new SQLUsuarios();

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        double alturaNueva = 0;

        String axu = modSQL.BuscarSemanaMala(Datos.get(this.ComCulti.getSelectedIndex()));
        String[] NSemanas = axu.split("-");
        int ss = 0;
        for (int i = 0; i < Datos.get(this.ComCulti.getSelectedIndex()).getSemana() + 1; i++) {
            if (String.valueOf(i + 1).equals(NSemanas[ss])) {
                dataset.addValue(alturaNueva, Datos.get(this.ComCulti.getSelectedIndex()).getNombreCultivo(), String.valueOf(i));
                if ((ss + 1) != NSemanas.length) {
                    ss++;
                }

            } else {
                dataset.addValue(alturaNueva, Datos.get(this.ComCulti.getSelectedIndex()).getNombreCultivo(), String.valueOf(i));
                alturaNueva = (i + 1) * CultivosCompa.get(Datos.get(this.ComCulti.getSelectedIndex()).getIdCultivo() - 1).getTasacCrsi();
                alturaNueva = Math.round(alturaNueva * 100) / 100d;
            }

        }

        //dataset.addValue(10, "1", "2");
        //dataset.addValue(5, "1", "3");
        JFreeChart chart = ChartFactory.createBarChart("Crecimiento", "Semanas", "Altura", dataset, PlotOrientation.VERTICAL,
                true, true, false);

        chartPanel1 = new ChartPanel(chart, false);
        this.jInternalFrame1.getContentPane().add(chartPanel1);
        this.jInternalFrame1.setVisible(true);
    }

    public void VerTodo() {

        DefaultCategoryDataset dataset1 = new DefaultCategoryDataset();
        int d = 0;
        for (Cultivo Dato : Datos) {

            dataset1.addValue(Dato.getAltura(), Dato.getNombreCultivo() + " " + d, Dato.getNombreCultivo() + "  " + d);
            d++;
        }

        //dataset.addValue(10, "1", "2");
        //dataset.addValue(5, "1", "3");
        JFreeChart chart = ChartFactory.createBarChart("Crecimiento", "Nombres", "Altura", dataset1, PlotOrientation.VERTICAL,
                true, true, false);

        this.jInternalFrame1.setVisible(false);
        chartPanel1 = new ChartPanel(chart, false);
        this.jInternalFrame1.getContentPane().remove(0);
        this.jInternalFrame1.getContentPane().add(chartPanel1);
        this.jInternalFrame1.setVisible(true);

    }

